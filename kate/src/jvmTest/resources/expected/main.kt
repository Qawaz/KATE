package output

// MathsClass provides functions for basic math operations
class MathsClass {
        
    // sumsTwoVars performs '+' between two variables
    fun sumsTwoVars() : Int {
         val variableOne = 4
         val variableTwo = 6
         return variableOne + variableTwo
    }
    
    // subtractTwoVars performs '-' between two variables
    fun subtractTwoVars() : Int {
         val variableOne = 6
         val variableTwo = 4
         return variableOne - variableTwo
    }
    
    // multiplyTwoVars performs '*' between two variables
    fun multiplyTwoVars() : Int {
         val variableOne = 4
         val variableTwo = 6
         return variableOne * variableTwo
    }
    
    // divideTwoVars performs '/' between two variables
    fun divideTwoVars() : Int {
         val variableOne = 4
         val variableTwo = 2
         return variableOne / variableTwo
    }


}